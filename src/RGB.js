/**
 * Data structure for rgb.
 */
export class RGB {
  /**
   * Initialises the object.
   *
   * @param {number} red - The red value.
   * @param {number} green - The green value.
   * @param {number} blue - The blue value.
   */
  constructor (red, green, blue) {
    this.red = red
    this.green = green
    this.blue = blue
  }
}
